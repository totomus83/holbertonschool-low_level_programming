#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - add 2 integers
 * @a: first int
 * @b: second int
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 integers
 * @a: first int
 * @b: second int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply 2 integers
 * @a: first int
 * @b: second int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 integers
 * @a: first int
 * @b: second int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - give the rest of the equation of the 2 integers
 * @a: first int
 * @b: second int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
